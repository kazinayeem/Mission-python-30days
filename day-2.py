# Day 2: Control Flow

# Example 1: If statement
x = 10

if x > 0:
    print("x is positive")
elif x == 0:
    print("x is zero")
else:
    print("x is negative")

# Example 2: For loop
fruits = ["apple", "banana", "cherry"]

print("Fruits in the list:")
for fruit in fruits:
    print(fruit)

# Example 3: While loop
num = 1

print("Counting from 1 to 5:")
while num <= 5:
    print(num)
    num += 1

print("Done!")
